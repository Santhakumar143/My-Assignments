package week1.day2;

public class LearnLoopingStatement {

	public static void main(String[] args) {

		int  number=5;
		
		for (int i = 0; i < number; i++) {
		
			System.out.println(i);
		}
		
		System.out.println();
		
		for (int i = 5; i >=0; i--) {
			
			System.out.println(i);
		}

	}

}