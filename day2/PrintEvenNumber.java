public class PrintEvenNumber {

	public static void main(String[] args) {

		int num=20;
		
		System.out.println("Even Number");
		System.out.println();
		
		for (int i = 0; i <=num; i++) {
			
			if (i%2!=1) {
			
				System.out.println(i);
			}
			 
		}

	}

}
