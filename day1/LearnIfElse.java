package week1.day1;

public class LearnIfElse {

	public static void main(String[] args) {

		int num=0;
		
		if (num>0) {
			
			System.out.println("Positive Number: "+num);
		}
		
		else if (num==0) {
			
			System.out.println("Number is Neutral: "+num);
		}

		else {
			
			System.out.println("Negative Number: "+num);
			
		}
	}

}
